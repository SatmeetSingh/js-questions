// Can I use reserved words as identifiers
// No

// An identifier is simply a name.
// identifiers are used to name variables and functions and to provide labels for certain loops in JavaScript code
// JavaScript identifier must begin with a letter, an underscore (_), or a dollar sign ($).
// i
// my_variable_name
// v13
// _dummy
// $str

// JavaScript reserves certain identifiers for use by the language itself. These “reserved words” cannot be used as regular identifiers.
// You cannot use these words as identifiers

// break
//  delete
//  function
//  return
//  typeof
//  case
//  do
//  if
//  switch
//  var
// catch
//  else
//  in
//  this
//  void
//  continue
// false
//  instanceof ...
